#加減乘除
#加法
apples = 3
#原先程式碼
apples = apples +1
#簡化加法程式碼
apples +=1
print(apples)
#減法
apples = apples-1
#簡化減法程式碼
apples -=1
print(apples)
#乘法
apples = apples*2
print(apples)
#簡化乘法程式碼
apples*=2
#除法
apples = apples/2
print(apples)
#簡化除法程式碼
apples/=2
print(apples)

#指數(平方,三平方)
apples = 3
print(apples)
#簡化指數程式碼
apples**=3
#模數mod
#10mod3等於3除1
print(10%3)
#11mod3等於3除2
print(11%3)
#12mod3等於4除0
print(12%3)
#次方pow
print(pow(2,5))
#最大值Max與最小值Min
x = 1
y = 2
z = 3
print(max(x,y,z))
print(min(x,y,z))
# 四捨五入round
a = 3.14
print(round(a))
b = 3.5
print(round(b))
#絕對值
c = -4
